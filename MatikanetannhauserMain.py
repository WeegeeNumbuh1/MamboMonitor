def gif_switcher() -> None:
    """ Thread that switches the gif to use. """
    main_logger.debug("Switch timer thread started.")
    warmup_inc = 0
    while True:
        dispatcher.send(message='', signal=SWITCH_GIF, sender=gif_switcher)
        if warmup_inc > 0:
            with warmup_done:
                warmup_done.wait_for(lambda: NEW_GIF, GIF_TIME / 2)
            time.sleep(GIF_TIME)
        else:
            time.sleep(5)
            warmup_inc += 1

