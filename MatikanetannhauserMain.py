def centerer(img_dim: int, canvas_dim: int) -> int:
    """ Provide an image dimension (height or width)
    and the corresponding canvas dimension, returns corresponding offset. """
    if img_dim > canvas_dim:
        return 0
    return (canvas_dim - img_dim) // 2

def has_transparency(img: Image.Image):
    if img.info.get("transparency", None) is not None:
        return True
    if img.mode == "P":
        transparent = img.info.get("transparency")
        if transparent is not None:
            for _, index in img.getcolors():
                if index == transparent:
                    return True
    elif img.mode == "RGBA":
        extrema = img.getextrema()
        if extrema[3][0] < 255: # Check the min value of alpha
            return True
    return False

