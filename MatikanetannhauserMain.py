class GIFProcessor:
    """ Handles the gif processing in a separate thread. Triggered by
    `gif_switcher`. """
    def __init__(self):
        self.loop = asyncio.new_event_loop()
        asyncio.set_event_loop(self.loop)
        register_signal_handler(self.loop, self.load_gif, signal=SWITCH_GIF, sender=gif_switcher)
        register_signal_handler(self.loop, self.end_thread, signal=END_THREADS, sender=sigterm_handler)
        self.gif_pool = set(gifs_paths)
        self.run_loop()

    def load_gif(self, message):
        # pick a random gif to play and pop it from the pool, then repeat
        # until we played all gifs in the directory
        global global_frames, global_durations, global_num_frames
        global NEW_GIF, global_img_height, global_img_width, gif_counter
        gif_path = random.choice(list(self.gif_pool))
        self.gif_pool.discard(gif_path)
        if len(self.gif_pool) == 0:
            self.gif_pool = set(gifs_paths)
            main_logger.info("Cycled through all gifs, going at it again")

        preprocess_start = time.perf_counter()
        try:
            gif = Image.open(gif_path)
            num_frames = gif.n_frames
            if has_transparency(gif):
                flatten = True
                flattened = Image.new("RGB", (gif.width, gif.height), (0,0,0))
            else:
                flatten = False
        except Exception:
            return

        # Preprocess the gifs frames into canvases to improve playback performance
        main_logger.debug(f"Picked \'{gif_path}\' | {num_frames} frames | ({gif.width}x{gif.height}) | Alpha: {flatten}")
        frames = []
        durations = []

        # gif-specific stuff:
        # https://pillow.readthedocs.io/en/stable/handbook/image-file-formats.html#gif
        for frame_index in range(0, num_frames):
            gif.seek(frame_index)
            durations.append(gif.info.get('duration', 40) / 1000) # grab each frame's duration in sec
            # must copy the frame out of the gif, since thumbnail() modifies the image in-place
            frame = gif.copy()
            if flatten:
                bg = flattened.copy()
                bg.paste(frame, mask=frame.split()[3])
                bg.thumbnail((canvas.width, canvas.height), Image.LANCZOS)
                frames.append(bg.convert("RGB"))
            else:
                frame.thumbnail((canvas.width, canvas.height), Image.LANCZOS)
                frames.append(frame.convert("RGB"))
        if flatten:
            wid = bg.width
            hei = bg.height
        else:
            wid = frame.width
            hei = frame.height

        # Close the gif file to save memory now that we have copied out all of the frames
        gif.close()
        # set minimum frame time for 1 frame gifs
        if len(durations) == 1:
            durations[0] = 0.1
        with frames_lock:
            global_frames = frames
            global_durations = durations
            global_num_frames = num_frames
            global_img_width = wid
            global_img_height = hei
        gif_counter += 1
        if gif_counter % 100 == 0:
            main_logger.info(f"Played {gif_counter} gifs so far!")

        proc_time = time.perf_counter() - preprocess_start
        gif_length = sum(durations)
        main_logger.debug(
            f"Completed processing in {proc_time:.3f} sec (~{num_frames / proc_time:.3f} FPS), "
            f"displaying gif ({gif_length:.3f} sec duration, ~{GIF_TIME/gif_length:.1f} loops, "
            f"resized: {wid}x{hei})")
        NEW_GIF = True
        with warmup_done:
            warmup_done.notify_all()

    def run_loop(self):
        def keep_alive():
            self.loop.call_later(1, keep_alive)
        keep_alive()
        self.loop.run_forever()

    def end_thread(self, message):
        self.loop.stop()

